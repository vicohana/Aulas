# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# só exitem duas respostas possiveis: 
# sim(True) ou não(False)
# Existem vários operadores para "questionar".
# Dentre ele o ==, que é um operador lógico que 
# questiona se um valor é igual a outro
print(10 == 10) #Sim => True (SEMPRE COMEÇA COM LETRA MAIUSCULA)
print(10 == 11) #Não => False (SEMPRE COMEÇA COM LETRA MAIUSCULA)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))

