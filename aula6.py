# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool 
print(1 + 1)
#para str a soma o python concatena as str,
# porém isso não acontece com str e outro tipo de variavel
